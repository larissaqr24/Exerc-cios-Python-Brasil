'''Faça um Programa que peça as 4 notas bimestrais e mostre a média.'''

primeiro_bimestre = int(input('Digite sua nota do 1º Bimestre: '))
segundo_bimestre = int(input('Digite sua nota do 2º Bimestre: '))
terceiro_bimestre = int(input('Digite sua nota do 3º Bimestre: '))
quarto_bimestre = int(input('Digite sua nota do 4º Bimestre: '))
print((primeiro_bimestre + segundo_bimestre + terceiro_bimestre + quarto_bimestre) /4)


